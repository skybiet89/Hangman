        if(word.length()>10)
        {
            JOptionPane.showMessageDialog(this, "Words more than 10 character \nlength are not allowed!");
            jPasswordField1.setText("");
        }
        else if(word.length()==0){
            JOptionPane.showMessageDialog(this, "Please enter a word!");
        }
        else{
            boolean flag=true;
            for(int i=0; i<word.length(); i++){
                if(!Character.isLetter(word.charAt(i))){
                    flag=false;
                }
            }
            if(flag){
                new board(word).setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "All characters must be Letters!");
                jPasswordField1.setText("");
            }
        }
       
